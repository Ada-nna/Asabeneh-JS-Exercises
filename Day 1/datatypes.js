// Q5. Create datatypes.js file and use the JavaScript typeof operator to check different data types. Check the data type of each variable

let newStringTwo = "My name is Cynthia";
console.log(typeof newStringTwo);  /* console returns string */

let string3 = ""
console.log(typeof string3);  /* console returns string */

let newBooleanTwo = !false
console.log(typeof newBooleanTwo);  /* console returns boolean */

let anotherBooleanTwo = !true;
console.log(typeof anotherBooleanTwo);  /* console returns boolean */

let undefinedValueTwo = undefined
console.log(typeof undefinedValueTwo);  /* console returns undefined */

let newVariableTwo;
console.log(typeof newVariableTwo);  /* console returns undefined */

let nullValueTwo = null
console.log(typeof nullValueTwo);  /* console returns object */